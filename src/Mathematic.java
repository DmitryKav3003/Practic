public class Mathematic {
    public static void main(String[] args) {
        double x = java.lang.Math.round(34.4);
        System.out.println(x);
        int y = java.lang.Math.min(34,55);
        System.out.println(y);
        int f = java.lang.Math.abs(-545);
        System.out.println(f);
        double g = java.lang.Math.sqrt(36);       //выводит квадрат из числа
        System.out.println(g);
        double z = java.lang.Math.tan(32);        //выводит тангенс)))
        System.out.println(z);
        double a = java.lang.Math.ceil(56.1);     //округляет число до целого вверх
        System.out.println(a);
        double p = java.lang.Math.floor(35.8);    //округляет число до целого вниз
        System.out.println(p);
        double h = java.lang.Math.asin(44);
        System.out.println(h);
    }
}
